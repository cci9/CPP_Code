#include<iostream>
#include<cmath>
#include<string>

using namespace std;

int main()
{
    int a;
    cout<<"size of a "<<sizeof(a)<<endl;

    float b;
    cout<<"size of b "<<sizeof(b)<<endl;

    char c;
    cout<<"size of c "<<sizeof(c)<<endl;

    bool d;
    cout<<"size of d "<<sizeof(d)<<endl;

    long int e;
    cout<<"size of e "<<sizeof(e)<<endl;

    short int f;
    cout<<"size of f "<<sizeof(f)<<endl;
    return 0;
}